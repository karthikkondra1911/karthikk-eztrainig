a=int(input("enter the number"))
if a>=0 and a<=30:
    print("the number is in the range")
if a>=0 and a<=10:
    print("the number is in the range 0 to 10")
elif a>10 and a<=20:
    print("the number is in the range 10 to 20")
elif a>20 and a<=30:
    print("the number is in the range 20 to 30")
else :
    print("the number is not in range")
